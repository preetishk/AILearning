"""
LLM as Judge Evaluator

LLM as Judge utilizes a large language model (e.g., Llama 3.1, Gemma, Qwen) to evaluate
candidate responses against reference responses. Unlike n-gram metrics (BLEU, ROUGE)
which only look for exact word overlaps, an LLM judge evaluates semantic accuracy,
completeness, style, reasoning quality, and factuality.

Features:
---------
1. Implements structured prompting to guide the LLM's evaluation.
2. Uses Ollama's local API (specifically `/api/chat` with JSON format option) for reproducible evaluation.
3. Automatically parses scores (1-5 scale) and qualitative reasoning.
4. Handles fallback and lists pulled models to provide clean error messages.
"""

import json
import logging
import requests
from typing import Dict, Any, List, Optional, Tuple

# Setup logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("LLMAsJudge")

class LLMAsJudgeEvaluator:
    def __init__(self, host: str = "http://localhost:11434", model: str = "llama3.1:latest", temperature: float = 0.0):
        self.host = host.rstrip('/')
        self.model = model
        self.temperature = temperature
        self._is_available = None

    def check_ollama_status(self) -> Tuple[bool, str]:
        """
        Checks if Ollama is running and if the selected model is pulled.
        Returns (is_available, status_message).
        """
        try:
            # Check if server is running
            response = requests.get(f"{self.host}/api/tags", timeout=3)
            if response.status_code != 200:
                return False, f"Ollama server returned status code {response.status_code}"
            
            data = response.json()
            pulled_models = [m["name"] for m in data.get("models", [])]
            
            # Check if specific model exists (exact match or without tag)
            model_exists = False
            for pm in pulled_models:
                if pm == self.model or pm.split(':')[0] == self.model.split(':')[0]:
                    model_exists = True
                    # Update to the actual tag name if it was fuzzy matched
                    self.model = pm
                    break
                    
            if not model_exists:
                available_str = ", ".join(pulled_models) if pulled_models else "None"
                return False, f"Model '{self.model}' is not pulled in Ollama. Available local models: [{available_str}]. Please run: 'ollama pull {self.model}'"
                
            return True, "Ollama is ready and model is available."
            
        except requests.exceptions.RequestException as e:
            return False, f"Could not connect to Ollama server at {self.host}. Is Ollama running? Error: {str(e)}"

    def build_evaluation_prompt(self, prompt: str, reference: str, candidate: str, metrics: List[Dict[str, str]]) -> str:
        """
        Builds the detailed, structured prompt for the LLM judge.
        The JSON schema is generated dynamically from the metrics list so that
        adding or removing metrics in config.yaml is automatically reflected here.
        """
        metrics_description = ""
        for m in metrics:
            metrics_description += f"- {m['name'].upper()}: {m['description']}\n"

        # Build the expected JSON schema dynamically from the metrics list
        schema_lines = []
        for i, m in enumerate(metrics):
            comma = "," if i < len(metrics) - 1 else ""
            schema_lines.append(
                f'    "{m["name"]}": {{\n'
                f'      "score": <int 1-5>,\n'
                f'      "reasoning": "<concise reason>"\n'
                f'    }}{comma}'
            )
        metrics_schema = "\n".join(schema_lines)

        user_content = f"""
You are an expert, impartial evaluation judge. Your task is to rate the quality of a candidate response generated by an AI assistant against a high-quality reference response, considering the original prompt.

[Original Prompt]
{prompt}

[Reference Ground Truth Response]
{reference}

[Candidate Response under Evaluation]
{candidate}

[Evaluation Criteria]
Evaluate the candidate response based on the following metrics:
{metrics_description}
For each metric, provide a score from 1 to 5 (1 being poor, 5 being perfect), along with a concise step-by-step reasoning explaining your score based on the candidate's quality compared to the reference response.

You must respond ONLY with a JSON object. Do not include markdown formatting or conversational filler. The JSON must follow this exact structure:
{{
  "metrics": {{
{metrics_schema}
  }},
  "overall_explanation": "<overall synthesis of candidate performance>"
}}
"""
        return user_content

    def evaluate(self, prompt: str, reference: str, candidate: str, metrics: List[Dict[str, str]]) -> Dict[str, Any]:
        """
        Sends the evaluation prompt to the local Ollama instance and returns the parsed scores.
        """
        is_ready, msg = self.check_ollama_status()
        if not is_ready:
            return {
                "success": False,
                "error": msg,
                "scores": {},
                "overall_explanation": "Skipped due to Ollama/model unavailability."
            }

        system_instruction = "You are a strict, objective, and analytical AI evaluator. You output pure, raw JSON matches."
        user_prompt = self.build_evaluation_prompt(prompt, reference, candidate, metrics)

        payload = {
            "model": self.model,
            "messages": [
                {"role": "system", "content": system_instruction},
                {"role": "user", "content": user_prompt}
            ],
            "options": {
                "temperature": self.temperature
            },
            "format": "json",
            "stream": False
        }

        try:
            url = f"{self.host}/api/chat"
            response = requests.post(url, json=payload, timeout=60)
            if response.status_code != 200:
                return {
                    "success": False,
                    "error": f"Ollama API returned HTTP {response.status_code}: {response.text}",
                    "scores": {}
                }

            result_data = response.json()
            message_content = result_data.get("message", {}).get("content", "")
            
            # Parse the JSON response
            evaluation = json.loads(message_content)
            
            return {
                "success": True,
                "model_used": self.model,
                "scores": evaluation.get("metrics", {}),
                "overall_explanation": evaluation.get("overall_explanation", "")
            }
            
        except json.JSONDecodeError as je:
            return {
                "success": False,
                "error": f"Failed to parse LLM judge response as JSON. Raw output: {message_content}",
                "scores": {}
            }
        except requests.exceptions.RequestException as re:
            return {
                "success": False,
                "error": f"Network request to Ollama failed during evaluation: {str(re)}",
                "scores": {}
            }
        except Exception as e:
            return {
                "success": False,
                "error": f"Unexpected error during LLM evaluation: {str(e)}",
                "scores": {}
            }
