"""
BLEU (Bilingual Evaluation Understudy) Evaluator

BLEU is a metric for evaluating the quality of text which has been machine-translated
from one natural language to another, or generated by a language model.
It measures the precision of n-grams (sequences of n consecutive words) in the candidate
text against the reference text, adjusted with a Brevity Penalty.

Mathematical Foundation:
------------------------
1. Modified N-Gram Precision (p_n):
   For each n-gram size (usually n=1..4), we calculate how many n-grams in the candidate
   appear in the reference. To avoid inflating scores by repeating the same word,
   we "clip" the candidate counts to the maximum count of that n-gram in the reference:
   
   p_n = (Sum_{n-gram in candidate} Min(Count_cand(n-gram), Max_ref_count(n-gram))) /
         (Sum_{n-gram in candidate} Count_cand(n-gram))

2. Brevity Penalty (BP):
   BLEU does not have a recall component, so short candidate answers can achieve high
   precision. To penalize translations that are too short, a Brevity Penalty is applied:
   
   BP = 1                      if c > r
   BP = exp(1 - r / c)         if c <= r
   
   where c = candidate word length, r = reference word length.

3. Final BLEU Score:
   BLEU = BP * exp( Sum_{n=1}^N w_n * log(p_n) )
   where w_n are weights (usually 0.25 for N=4).
"""

import math
import re
from collections import Counter
from typing import Dict, List, Tuple, Any
import nltk
from nltk.translate.bleu_score import sentence_bleu, SmoothingFunction


def tokenize(text: str) -> List[str]:
    """
    Cleans and tokenizes text by lowecasing and splitting on word boundaries.
    Does not require external NLTK downloads, ensuring reliability.
    """
    text = text.lower()
    # Replace punctuation with spaces and split
    words = re.findall(r'\b\w+\b', text)
    return words


def get_ngrams(tokens: List[str], n: int) -> List[Tuple[str, ...]]:
    """Generates n-grams of size n from a list of tokens."""
    return [tuple(tokens[i:i + n]) for i in range(len(tokens) - n + 1)]


def calculate_manual_bleu(reference: str, candidate: str, weights: List[float] = [0.25, 0.25, 0.25, 0.25]) -> Dict[str, Any]:
    """
    Calculates BLEU score manually to demonstrate step-by-step calculations.
    """
    ref_tokens = tokenize(reference)
    cand_tokens = tokenize(candidate)
    
    c = len(cand_tokens)
    r = len(ref_tokens)
    
    if c == 0:
        return {"bleu": 0.0, "precisions": [], "bp": 0.0, "details": "Candidate has 0 tokens."}

    precisions = []
    max_n = len(weights)
    
    ngram_details = {}
    
    for n in range(1, max_n + 1):
        cand_ngrams = get_ngrams(cand_tokens, n)
        ref_ngrams = get_ngrams(ref_tokens, n)
        
        if not cand_ngrams:
            # If candidate is shorter than n, precision for this n is 0.0
            precisions.append(0.0)
            ngram_details[f"{n}-gram"] = {"overlap": 0, "total": 0, "precision": 0.0}
            continue
            
        cand_counts = Counter(cand_ngrams)
        ref_counts = Counter(ref_ngrams)
        
        # Calculate clipped overlap count
        clipped_overlap = 0
        for ngram, count in cand_counts.items():
            ref_max = ref_counts.get(ngram, 0)
            clipped_overlap += min(count, ref_max)
            
        total_cand_ngrams = len(cand_ngrams)
        p_n = clipped_overlap / total_cand_ngrams
        precisions.append(p_n)
        
        ngram_details[f"{n}-gram"] = {
            "overlap": clipped_overlap,
            "total": total_cand_ngrams,
            "precision": p_n
        }

    # Brevity Penalty
    if c > r:
        bp = 1.0
    else:
        bp = math.exp(1 - r / c) if c > 0 else 0.0
        
    # Final BLEU score calculation (Geometric Mean of precisions)
    # We use a simple smoothing: if precision is 0, we can use a tiny value to prevent log(0) from failing
    smoothed_precisions = []
    for p in precisions:
        if p == 0:
            smoothed_precisions.append(1e-9)  # Minimal smoothing
        else:
            smoothed_precisions.append(p)
            
    weighted_log_sum = sum(w * math.log(p) for w, p in zip(weights, smoothed_precisions))
    bleu_score = bp * math.exp(weighted_log_sum)
    
    # If all precisions are 0 (no overlap at all), BLEU is 0
    if all(p == 0 for p in precisions):
        bleu_score = 0.0

    return {
        "score": bleu_score,
        "brevity_penalty": bp,
        "precisions": precisions,
        "details": ngram_details,
        "reference_len": r,
        "candidate_len": c
    }


def calculate_nltk_bleu(reference: str, candidate: str, weights: Tuple[float, ...] = (0.25, 0.25, 0.25, 0.25)) -> float:
    """
    Calculates BLEU score using NLTK's sentence_bleu library for validation.
    """
    ref_tokens = tokenize(reference)
    cand_tokens = tokenize(candidate)
    
    # We use Method 1 smoothing (adds a very small value to 0 precision n-grams)
    # to handle cases where some higher n-gram precisions are 0.
    chencherry = SmoothingFunction()
    
    try:
        # NLTK expects references as a list of lists of tokens
        score = sentence_bleu([ref_tokens], cand_tokens, weights=weights, smoothing_function=chencherry.method1)
        return score
    except Exception:
        return 0.0


def calculate_bleu(reference: str, candidate: str, weights: List[float] = [0.25, 0.25, 0.25, 0.25]) -> Dict[str, Any]:
    """
    Wrapper function that computes both manual and NLTK BLEU for comparison.
    """
    manual_res = calculate_manual_bleu(reference, candidate, weights)
    nltk_val = calculate_nltk_bleu(reference, candidate, tuple(weights))
    
    return {
        "manual": manual_res,
        "nltk_reference": nltk_val,
        "score": manual_res["score"],  # Default score exposed
        "brevity_penalty": manual_res["brevity_penalty"],
        "precisions": manual_res["precisions"]
    }
